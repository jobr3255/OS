/*
 *  Returns:
 *   1 if unallocated page found
 *   0 if all pages are already allocated
 */
static int freePage(Pentry q[MAXPROCESSES], int proc) {
	int page;
	for(page = 0; page < MAXPROCPAGES; page++) {
        // unallocated so return true
		if(!q[proc].pages[page])
			return TRUE;
	}
	return FALSE;
}

static long alldone (Pentry q[MAXPROCESSES]) {
    int i;
    for (i=0; i<MAXPROCESSES; i++) {
	if (q[i].active) return 0;
    }
    return 1;
}

static void lru_pageit(Pentry q[MAXPROCESSES]) {
	int proc;
    int page;
    int outPage;
    int pageTick;
	int lastswap = -1;

	for(proc = 0; proc < MAXPROCESSES; proc++) {
        // skip itteration if process is inactive
		if(!q[proc].active){
            continue;
        }
        // set page according to the program counter
        page = q[proc].pc/PAGESIZE;
        // set timestamp for this page
		timestamps[proc][page] = tick;

		// skip itteration if page is already in memory
		if(q[proc].pages[page]){
            continue;
        }
        // continue if process is successfully swapped in
		if(pagein(proc, page)){
			if (lastswap != -1) {
				pageProb[lastswap][page]+=1;
			}
            if (lastswap != -1) {
                procPageProb[proc][lastswap][page]+=1;
            }
            lastswap = page;
            continue;
        }
        // skip itteration if no free page
		if(!freePage(q, proc))
			continue;

        // set evict page to -1 in case no page is found
        outPage = -1;

    	for(page = 0; page < MAXPROCPAGES; page++) {
            // cant evict unallocated page
    		if(!q[proc].pages[page]){
                continue;
            }

    		if(timestamps[proc][page] < tick + 1) {
    			pageTick = timestamps[proc][page];
    			outPage = page;
                // this will be always be the most recently used page
    			if(pageTick <= 1)
    				break;
    		}
    	}
        // start swap-out so break loop
        if(pageout(proc, outPage)) {
            break;
	    }
	}
}

void pageit(Pentry q[MAXPROCESSES]) {
    int proctmp;
    int pagetmp;

    if(!initialized){
    	for(proctmp=0; proctmp < MAXPROCESSES; proctmp++){
    	    for(pagetmp=0; pagetmp < MAXPROCPAGES; pagetmp++){
                timestamps[proctmp][pagetmp] = 0;
    	    }
    	}
		fp = fopen ("pageProb.txt","w");
        initialized = 1;
    }

    lru_pageit(q);


	if(alldone(q)){
		double sum[15];
		fprintf(fp, "Counts\n");
		for (int i = 0; i < 15; i++) {
			sum[i] = 0;
			fprintf(fp, "[");
			printf("[");
			for (int j = 0; j < 15; j++) {
				if(i != j){
					sum[i] += pageProb[i][j];
					fprintf(fp, "%d, ", pageProb[i][j]);
					printf("%d, ", pageProb[i][j]);
				}else{
					fprintf(fp, "%d, ", 0);
					printf("%d, ", 0);
				}
			}
			fprintf(fp, "]\n");
			printf("]\n");
		}
        for(proctmp=0; proctmp < MAXPROCESSES; proctmp++){
            fprintf(fp, "Proc %d \n", proctmp);
            for (int i = 0; i < 15; i++) {
    			sum[i] = 0;
    			fprintf(fp, "[");
    			for (int j = 0; j < 15; j++) {
    				if(i != j){
    					sum[i] += procPageProb[proctmp][i][j];
    					fprintf(fp, "%d, ", procPageProb[proctmp][i][j]);
    				}else{
    					fprintf(fp, "%d, ", 0);
    				}
    			}
    			fprintf(fp, "]\n");
    		}
        }
	}

    tick++;
}
